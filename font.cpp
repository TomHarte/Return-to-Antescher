float TextHeight;

void setuptext()
{
	TextHeight = 0;
	float alltexttop = 100, alltextbottom = -100;

	int c = NUM_CHARS;
	while(c--)
	{
		/* work out max vert */
		int maxvert = 0;
		int polyc = Alphabet[c].NumTriangles * 3;
		while(polyc--)
			if(Alphabet[c].CornerArray[polyc] > maxvert) maxvert = Alphabet[c].CornerArray[polyc];
		maxvert++;
	
		/* work out top left of char */
		float top = 100;
		float bottom = -100;
		float left = 100;
		float right = -100;
		int vc = maxvert;
		while(vc--)
		{
			if(Alphabet[c].VertArray[(vc*2) + 1] < top) top = Alphabet[c].VertArray[(vc*2) + 1];
			if(Alphabet[c].VertArray[(vc*2) + 1] > bottom) bottom = Alphabet[c].VertArray[(vc*2) + 1];
			if(Alphabet[c].VertArray[(vc*2)] < left) left = Alphabet[c].VertArray[(vc*2)];
			if(Alphabet[c].VertArray[(vc*2)] > right) right = Alphabet[c].VertArray[(vc*2)];
		}
		
		if(top < alltexttop) alltexttop = top;
		if(bottom > alltextbottom) alltextbottom = bottom;
		if(right - left > TextHeight) TextHeight = right - left;

		/* recentre character so they have a left of 0, fill in width, rotate by 90 degrees */
		Alphabet[c].Width = bottom - top;
		vc = maxvert;
		while(vc--)
		{
			float t = Alphabet[c].VertArray[(vc*2) + 1] - top;
			Alphabet[c].VertArray[(vc*2) + 1] = Alphabet[c].VertArray[(vc*2)];
			Alphabet[c].VertArray[(vc*2)] = t;
		}
	}
	
/*	properly centre all text vertically

	printf("Text range: %0.2f -> %0.2f\n", alltexttop, alltextbottom);

	c = NUM_CHARS;
	while(c--)
	{
	}*/

	TextHeight += STD_SPACING;
}

