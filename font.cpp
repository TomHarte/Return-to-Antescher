float __EBGF_TextWidth(char *spos)
{
	float width = 0;
	while(*spos)
	{
		int objnum = -1;

		if(*spos >= 48 && *spos <= 58)
			objnum = *spos - 48;

		if(*spos >= 65 && *spos <= 90)
			objnum = *spos - 55;

		if(*spos >= 97 && *spos <= 122)
			objnum = *spos - 61;
			
		if(objnum >= 0)
		{
			character *CPtr = &Alphabet[objnum];
			width += CPtr->Width + 0.05f;
		}
		else
			if(*spos == ' ')
				width += 0.35f;

		spos++;
	}

	return width;
}

float EBGF_TextWidth(char *str, ...)
{
	char TempBuf[2048];
	va_list Arguments;
	
	va_start(Arguments, str);
	vsprintf(TempBuf, str, Arguments);
	va_end(Arguments);
	return __EBGF_TextWidth(TempBuf);
}

