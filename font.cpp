						glVertex2f(0.05, 0.05 - 0.22);
						glVertex2f(-0.05, 0.05 - 0.22);
					glEnd();
					glTranslatef(0.15f, 0, 0);
				break;
				case ',':
					glTranslatef(0.07f, 0, 0);
					glBegin(GL_QUADS);
						glVertex2f(-0.05, -0.05 - 0.22);
						glVertex2f(0.05, -0.05 - 0.22);
						glVertex2f(0.05, 0.05 - 0.22);
						glVertex2f(-0.05, 0.05 - 0.22);
					glEnd();
					glBegin(GL_TRIANGLES);
						glVertex2f(0.05, -0.05 - 0.22);
						glVertex2f(-0.025, -0.05 - 0.22);
						glVertex2f(0, -0.2 - 0.22);
					glEnd();
					glTranslatef(0.15f, 0, 0);
				break;
				case '!':
					glTranslatef(0.15f, 0, 0);
					glBegin(GL_QUADS);
